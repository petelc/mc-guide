import { firebase } from "../../firebase";

const db = firebase.ref("/services");

const getAll = () => {
  return db;
};

const create = (data) => {
  return db.push(data);
};

const update = (key, data) => {
  return db.child(key).update(data);
};

const remove = (key) => {
  return db.child(key).remove();
};

export { getAll, create, update, remove };
